CREATE TABLE ADMINISTRATOR
(Fname VARCHAR(15) NOT NULL,
 Lname VARCHAR(15),
 Admin_ID VARCHAR(8) NOT NULL,
 Admin_pw VARCHAR(10) NOT NULL,
 Phone_no INT NOT NULL,
 PRIMARY KEY(Admin_id),
);

CREATE TABLE CASHIER
(Cashier_id VARCHAR(12) NOT NULL,
 Name VARCHAR(15) NOT NULL,
 phone_no INT NOT NULL,
 cashier_pw VARCHAR(10) NOT NULL,
 Admin_id VARCHAR(8) NOT NULL,
 PRIMARY KEY(Cashier_id),
 FOREIGN KEY(Admin_id)REFERENCES ADMINISTRATOR(Admin_ID)
);

CREATE TABLE SUPPLIER
(Supplier_ID VARCHAR(9) NOT NULL,
 Name VARCHAR(15) NOT NULL,
 phone_no INT NOT NULL,
 Cash_ID VARCHAR(12) NOT NULL,
 PRIMARY KEY(Supplier_ID),
 FOREIGN KEY(Cash_ID)REFERENCES CASHIER(Cashier_id)
);

CREATE TABLE CUSTOMER
(Fname VARCHAR(15) NOT NULL,
 Lname VARCHAR(15),
 Cust_Email_id VARCHAR(25),
 Cust_Pone_no INT NOT NULL,
 Gender CHAR(1) NOT NULL,
 Date_Of_Birth DATE,
 Address VARCHAR(35) NOT NULL,
);

CREATE TABLE PRESCRIPTION
(Prescription_ID VARCHAR(7) NOT NULL,
 Patient_name VARCHAR(15),
 Stock_id INT NOT NULL,
 Drug_name VARCHAR(15) NOT NULL,
 Cust_name VARCHAR(15) NOT NULL,
 Phone_no INT,
 UNIQUE (Stock_id),
 PRIMARY KEY(Prescription_ID),
 FOREIGN KEY(Cust_name)REFERENCES CUSTOMER(Fname),
 FOREIGN KEY(Phone_no)REFERENCES CUSTOMER(Cust_Phone_no)
);

CREATE TABLE STOCK
(Stock_ID INT NOT NULL,
 Company VARCHAR(15) NOT NULL,
 Drug_name VARCHAR(15) NOT NULL, 
 Qunatity INT,
 Cost INT,
 Description VARCHAR(15),
 Expiry_Date DATE,
 Admin_ID VARCHAR(8) NOT NULL
 PRIMARY KEY(Drug_name),
 FOREIGN KEY(Stock_ID)REFERENCES PRESCRIPTION(Stock_id)
);

CREATE TABLE ORDER
(Invoice_no INT NOT NULL,
 Prescription_ID VARCHAR(7) NOT NULL,
 Cost INT,
 Delivery_Address VARCHAR(35) NOT NULL,
 Cashier_ID VARCHAR(12) NOT NULL,
 Supplier_ID VARCHAR(9) NOT NULL,
 PRIMARY KEY(Invoice_no),
 FOREIGN KEY(Prescription_ID)REFERENCES PRESCRIPTION(Prescription_ID),
 FOREIGN KEY(Cashier_ID)REFERENCES CASHIER(Cashier_id),
 FOREIGN KEY(Supplier_ID)REFERENCES SUPPLIER(Supplier_ID),
 FOREIGN KEY(Delivery_Address)REFERENCES CUSTOMER(Address)
);



